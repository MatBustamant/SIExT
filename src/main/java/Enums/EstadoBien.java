public enum EstadoBien {
    EN_CONDICIONES("EN CONDICIONES"),
    AVERIADO("AVERIADO");

    private final String nombre;

    private EstadoBien(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return nombre;
    }
}
