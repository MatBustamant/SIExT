public enum RolUsuario {
    ADMINISTRATIVO("ADMINISTRATIVO"),
    SUPERVISOR("SUPERVISOR");
    
    private final String nombre;

    private RolUsuario(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return nombre;
    }
}
